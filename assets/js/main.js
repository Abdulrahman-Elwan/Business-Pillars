let currentLang = localStorage.getItem('lang') || 'en';

function applyTranslation(lang) {
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (translate[lang][key]) {
            el.innerHTML = translate[lang][key];
        }
    });

    const flagImg = document.getElementById("language-flag");
    if (flagImg) {
        flagImg.src = translate[lang].language_flag;
        flagImg.alt = translate[lang].language_switch;
    }

    document.documentElement.lang = lang;
    localStorage.setItem('lang', lang);

    applyDirection(lang);
    updateNavAlignment(lang);
    updateFontFamily(lang);
    applyDirection(lang);
}

function applyDirection(lang) {
    const direction = lang === 'ar' ? 'rtl' : 'ltr';

    document.querySelectorAll('.lang-dir').forEach(el => {
        el.setAttribute('dir', direction);
    });
}

function updateNavAlignment(lang) {
    const nav = document.getElementById('navbarNav');
    if (!nav) return;

    if (lang === 'en') {
        nav.classList.add('ms-auto');
        nav.classList.remove('me-auto');
    } else if (lang === 'ar') {
        nav.classList.add('me-auto');
        nav.classList.remove('ms-auto');
    }
}

function updateFontFamily(lang) {
    // تغيير الخط بناءً على اللغة
    if (lang === 'ar') {
        document.body.style.fontFamily = 'Cairo, sans-serif';
    } else {
        document.body.style.fontFamily = 'Nunito Sans, sans-serif';
    }
}

function applyDirection(lang) {
    const direction = lang === 'ar' ? 'rtl' : 'ltr';

    document.querySelectorAll('.lang-dir').forEach(el => {
        el.style.direction = direction;
        el.style.textAlign = direction === 'rtl' ? 'right' : 'left';
    });
}

// حدث تبديل اللغة
document.getElementById('lang-toggle').addEventListener('click', (e) => {
    e.preventDefault();
    currentLang = currentLang === 'en' ? 'ar' : 'en';
    applyTranslation(currentLang);
});

// تنفيذ الترجمة من أول تحميل
applyTranslation(currentLang);







// ******************* Navbar *******************
window.addEventListener('scroll', function () {
    const navbar = document.querySelector('.navbar');
    if (window.scrollY > 50) {
        navbar.classList.add('scrolled');
    } else {
        navbar.classList.remove('scrolled');
    }
});







